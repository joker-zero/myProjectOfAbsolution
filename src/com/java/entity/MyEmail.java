package com.java.entity;

public class MyEmail {
	private int emailId;
	private String acceptUserId;
	private String sendUserId;
	private String sendUserName;
	private String emailTheam;
	private String emailContext;
	private String acceptTime;
	private String emailState;
	public int getEmailId() {
		return emailId;
	}
	public void setEmailId(int emailId) {
		this.emailId = emailId;
	}
	public String getAcceptUserId() {
		return acceptUserId;
	}
	public void setAcceptUserId(String acceptUserId) {
		this.acceptUserId = acceptUserId;
	}
	public String getSendUserId() {
		return sendUserId;
	}
	public void setSendUserId(String sendUserId) {
		this.sendUserId = sendUserId;
	}
	public String getSendUserName() {
		return sendUserName;
	}
	public void setSendUserName(String sendUserName) {
		this.sendUserName = sendUserName;
	}
	public String getEmailTheam() {
		return emailTheam;
	}
	public void setEmailTheam(String emailTheam) {
		this.emailTheam = emailTheam;
	}
	public String getEmailContext() {
		return emailContext;
	}
	public void setEmailContext(String emailContext) {
		this.emailContext = emailContext;
	}
	public String getAcceptTime() {
		return acceptTime;
	}
	public void setAcceptTime(String acceptTime) {
		this.acceptTime = acceptTime;
	}
	public String getEmailState() {
		return emailState;
	}
	public void setEmailState(String emailState) {
		this.emailState = emailState;
	}
}
